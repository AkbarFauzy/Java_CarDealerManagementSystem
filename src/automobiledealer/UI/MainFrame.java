    private void EmployeeForm_ComboBox_mmItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_EmployeeForm_ComboBox_mmItemStateChanged
       switch(EmployeeForm_ComboBox_mm.getSelectedItem().toString()){
           case "FEB":
               int tresh = 28;
               if((Integer.parseInt(EmployeeForm_ComboBox_yyyy.getSelectedItem().toString())%4) == 0 && (Integer.parseInt(EmployeeForm_ComboBox_yyyy.getSelectedItem().toString())% 100) != 0){
                   tresh = 29;
               }
               DefaultComboBoxModel CB = new DefaultComboBoxModel();
               for(int i=1;i<=tresh;i++){
                    CB.addElement(i);
               }
               EmployeeForm_ComboBox_dd.setModel(CB);
               break;
           case "APR":
           case "SEP":
           case "NOV":
               ChangeDDto30();
               break;
           default:
               ChangeDDto31();
               
       }
    }//GEN-LAST:event_EmployeeForm_ComboBox_mmItemStateChanged

